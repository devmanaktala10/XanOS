void mp1_free(void* memory)
{
    ece391_memset(memory, 0, sizeof(struct mp1_blink_struct));
}

void ece391_memset(void* memory, char c, int n)
{
    char* mem = (char*)memory;
    int i;
    for(i=0; i<n; i++) {
        mem[i] = c;
    }
}

int32_t ece391_memcpy(void* dest, const void* src, int32_t n)
{
    int32_t i;
    char* d = (char*)dest;
    char* s = (char*)src;
    for(i=0; i<n; i++) {
        d[i] = s[i];
    }

    return 0;
}

